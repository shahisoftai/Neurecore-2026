// ─── TIER MAPPING ──────────────────────────────────────────────────────────

const TIER_LIMITS = {
  COMMUNITY: 3,
  STARTER: 6,
  PRO: 18,
  ENTERPRISE: 80,
};

const TIER_LABELS = {
  COMMUNITY: 'Community',
  STARTER: 'Starter',
  PRO: 'Professional',
  ENTERPRISE: 'Enterprise',
};

const RECOMMENDED = new Set([
  'SAAS:PRO', 'ECOMMERCE:PRO', 'MARKETING_AGENCY:PRO', 'GENERAL:PRO',
  'HEALTHCARE:PRO', 'LEGAL:PRO', 'FINANCE:PRO', 'CONSULTING:PRO',
  'PROFESSIONAL_SERVICES:PRO', 'ACCOUNTING:PRO', 'AUDIT:PRO',
]);

// ─── MAIN ──────────────────────────────────────────────────────────────────

async function main() {
  const t0 = Date.now();

  const tiers = await prisma.tier.findMany({ where: { isActive: true } });
  if (!tiers.length) throw new Error('No active tiers found. Seed tiers first.');
  const tierBySlug = new Map(tiers.map(t => [t.slug.toUpperCase(), t]));

  const poolAgents = await prisma.poolAgent.findMany({
    select: { id: true, slug: true, divisionSlug: true },
  });
  const agentBySlug = new Map(poolAgents.map(a => [a.slug, a]));

  const poolDepts = await prisma.poolDepartment.findMany({
    select: { id: true, slug: true },
  });
  const deptBySlug = new Map(poolDepts.map(d => [d.slug, d]));

  // Validate referenced slugs + divisions
  const referenced = new Set();
  const referencedDivs = new Set();
  for (const ind of Object.values(INDUSTRIES)) {
    for (const role of ind.roles) {
      referenced.add(role.poolAgentSlug);
      referencedDivs.add(role.divisionSlug);
    }
  }
  const missing = [...referenced].filter(s => !agentBySlug.has(s));
  if (missing.length) {
    console.error('[seed-inds] FATAL: missing PoolAgent slugs:');
    for (const s of missing) console.error('  - ' + s);
    process.exit(1);
  }
  const missingDivs = [...referencedDivs].filter(s => !deptBySlug.has(s));
  if (missingDivs.length) {
    console.error('[seed-inds] FATAL: missing PoolDepartment slugs:');
    for (const s of missingDivs) console.error('  - ' + s);
    process.exit(1);
  }

  const industries = Object.keys(INDUSTRIES);
  console.log(`[seed-inds] industries=${industries.length}, tiers=${tiers.length}, poolAgents=${poolAgents.length}, poolDepts=${poolDepts.length}`);

  // Build all packages
  const packagePlans = [];
  for (const [industryKey, ind] of Object.entries(INDUSTRIES)) {
    for (const [tierSlug, tier] of tierBySlug.entries()) {
      const tierUpper = tierSlug.toUpperCase();
      const limit = Math.min(TIER_LIMITS[tierUpper] || 18, tier.maxAgents);

      const sorted = [...ind.roles]
        .filter(r => agentBySlug.has(r.poolAgentSlug))
        .sort((a, b) => {
          if (a.isRequired !== b.isRequired) return a.isRequired ? -1 : 1;
          return (a.slot || 1) - (b.slot || 1);
        })
        .slice(0, limit);

      packagePlans.push({
        industry: industryKey,
        tierId: tier.id,
        tierSlug: tierUpper,
        tierLabel: TIER_LABELS[tierUpper] || tier.name,
        tierMaxAgents: tier.maxAgents,
        name: `${humanize(industryKey)} • ${TIER_LABELS[tierUpper] || tier.name}`,
        description: `${ind.description} Includes ${sorted.length} curated role${sorted.length === 1 ? '' : 's'} for ${tier.name} tier (limit ${tier.maxAgents}).`,
        isRecommended: RECOMMENDED.has(`${industryKey}:${tierUpper}`),
        entries: sorted,
      });
    }
  }

  console.log(`[seed-inds] packages to upsert: ${packagePlans.length}`);

  if (DRY_RUN) {
    let totalEntries = 0;
    const byTier = {};
    for (const p of packagePlans) {
      totalEntries += p.entries.length;
      byTier[p.tierSlug] = (byTier[p.tierSlug] || 0) + 1;
      console.log(`  ${p.industry.padEnd(22)} × ${p.tierSlug.padEnd(10)} → ${String(p.entries.length).padStart(3)} entries${p.isRecommended ? ' [recommended]' : ''}`);
    }
    console.log(`[seed-inds] total entries: ${totalEntries}`);
    console.log(`[seed-inds] by tier:`, byTier);
    return;
  }

  let pkgUpserts = 0;
  let entryInserts = 0;
  for (const plan of packagePlans) {
    const pkg = await prisma.industryPackage.upsert({
      where: { industry_tierId: { industry: plan.industry, tierId: plan.tierId } },
      update: {
        name: plan.name,
        description: plan.description,
        isRecommended: plan.isRecommended,
        isActive: true,
      },
      create: {
        industry: plan.industry,
        tierId: plan.tierId,
        name: plan.name,
        description: plan.description,
        isRecommended: plan.isRecommended,
        isActive: true,
      },
    });
    pkgUpserts++;

    // Neon pooler has a tight 5s transaction budget. deleteMany + createMany
    // are each idempotent so we can run them sequentially without a tx.
    await prisma.industryPackageEntry.deleteMany({ where: { packageId: pkg.id } });
    if (plan.entries.length) {
      await prisma.industryPackageEntry.createMany({
        data: plan.entries.map((e, idx) => {
          const agent = agentBySlug.get(e.poolAgentSlug);
          return {
            packageId: pkg.id,
            poolAgentId: agent.id,
            divisionSlug: e.divisionSlug,
            slot: e.slot || idx + 1,
            isRequired: e.isRequired !== false,
            isDefaultSelected: e.isDefaultSelected !== false,
            defaultBudgetPerDay: e.defaultBudgetPerDay || null,
            defaultModel: e.defaultModel || null,
          };
        }),
      });
    }
    entryInserts += plan.entries.length;
  }

  console.log(
    `[seed-inds] DONE in ${((Date.now() - t0) / 1000).toFixed(1)}s — packages=${pkgUpserts}, entries=${entryInserts}`,
  );
}

function humanize(s) {
  return s
    .toLowerCase()
    .split('_')
    .map(w => w[0].toUpperCase() + w.slice(1))
    .join(' ');
}

main()
  .catch(err => { console.error('[seed-inds] FAILED', err); process.exitCode = 1; })
  .finally(() => prisma.$disconnect());